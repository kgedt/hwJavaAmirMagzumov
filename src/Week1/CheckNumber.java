package Week1;

public class CheckNumber {

    public static boolean isPositive(int num) {
        return num > 0 ? true : false;
    }

    public static boolean isNegative(int num) {
        return num < 0 ? true : false;
    }

    public static boolean Zero(int num) {
        return 0 == num ? true : false;
    }
}
